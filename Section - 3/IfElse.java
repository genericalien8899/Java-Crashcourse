public class IfElse {
    public static void main(String[] args) {
        
        int grade = 75;
        if(grade>90)
        {
            System.out.println("Congrats, you have scored A");
        }
        else if(grade>=70)
        {
            System.out.println("Congrats you have passed");
        }
        else
        {
            System.out.println("You have failed");
        }
    }
}
